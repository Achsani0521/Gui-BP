    private void TambahPelangganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahPelangganActionPerformed
        if(jplg<mplg){
            //cek kesamaan ID
            boolean ada=false;
            for (int i = 0; i < jplg; i++) {
                if(plg[i].getID().equalsIgnoreCase
                    (jTextIdPelanggan.getText())){
                    ada=true;
                    break;
                }
            }
            if(ada){
                JOptionPane.showMessageDialog
                (this, "ID sudah ada!");
            }
            else{
                String kd = jTextIdPelanggan.getText();
                String nm = jTextNamaPelanggan.getText();
                String al = jTextAlamatPelanggan.getText();
                plg[jplg] = new cPelanggan(kd,nm,al);
                //siapkan array String utk ditampilkan di tabel
                String row[][] = new String[1][3];
                row[0][0] = plg[jplg].getID();
                row[0][1] = plg[jplg].getNama();
                row[0][2] =
                String.valueOf(plg[jplg].getAlamat());
                DefaultTableModel model =
                (DefaultTableModel)jTable1.getModel();
                model.addRow(row[0]);
                jplg++;
                JOptionPane.showMessageDialog(this,
                    "Penambahan sukses...");
                //kosongkan kembali textfield-nya form
                jTextIdPelanggan.setText("");
                jTextNamaPelanggan.setText("");
                jTextAlamatPelanggan.setText("");
            }
        }
        else{
            JOptionPane.showMessageDialog(this,
                "Kapasitas penuh!");
            //kosongkan kembali textfield-nya form
            jTextIdPelanggan.setText("");
            jTextNamaPelanggan.setText("");
            jTextAlamatPelanggan.setText("");
        }
    }//GEN-LAST:event_TambahPelangganActionPerformed

