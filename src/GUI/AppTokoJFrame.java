    private void HapusPelangganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusPelangganActionPerformed
        //cari dulu ID di array object pelanggan
        boolean ketemu=false;
        int indeks=0;
        String id = JOptionPane.showInputDialog(this,
            "Masukkan ID");
        for (int i = 0; i < jplg; i++) {
            indeks=i;
            if(plg[i].getID().equalsIgnoreCase(id)){
                ketemu=true;
                break;
            }
        }
        if(ketemu==true){
            int j = JOptionPane.showConfirmDialog(this,
                "Yakin dihapus?");
            if(j==0){
                for (int i = indeks; i < jplg; i++) {
                    if(i==jplg-1)
                    plg[i] = null;
                    else
                    plg[i]=plg[i+1];
                }
                jplg--;
                //update isi tabel
                DefaultTableModel model =
                (DefaultTableModel)jTable1.getModel();
                model.removeRow(indeks);
                JOptionPane.showMessageDialog(this,
                    "Berhasil dihapus...");
            }
            else JOptionPane.showMessageDialog(this,
                "Batal dihapus!");
        }
        else {
            JOptionPane.showMessageDialog(this, "ID tidak ada!");
        }
    }//GEN-LAST:event_HapusPelangganActionPerformed

