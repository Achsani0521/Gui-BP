    private void TambahBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahBarangActionPerformed
        if(jbrg<mbrg){
            //cek kesamaan ID
            boolean ada=false;
            for (int i = 0; i < jbrg; i++) {
                if(brg[i].getKode().equalsIgnoreCase
                    (jTextKodeBarang.getText())){
                    ada=true;
                    break;
                }
            }
            if(ada){
                JOptionPane.showMessageDialog
                (this, "ID sudah ada!");
            }
            else{
                String kd = jTextKodeBarang.getText();
                String nm = jTextNamaBarang.getText();
                String h = jTextHargaBarang.getText();
                brg[jbrg] = new cBarang(nm,kd,h);
                //siapkan array String utk ditampilkan di tabel
                String row[][] = new String[1][3];
                row[0][0] = brg[jbrg].getKode();
                row[0][1] = brg[jbrg].getNama();
                row[0][2] =
                String.valueOf(brg[jbrg].getHarga());
                DefaultTableModel model =
                (DefaultTableModel)jTable2.getModel();
                model.addRow(row[0]);
                jbrg++;
                JOptionPane.showMessageDialog(this,
                    "Penambahan sukses...");
                //kosongkan kembali textfield-nya form
                jTextKodeBarang.setText("");
                jTextNamaBarang.setText("");
                jTextHargaBarang.setText("");
            }
        }
        else{
            JOptionPane.showMessageDialog(this,
                "Kapasitas penuh!");
            //kosongkan kembali textfield-nya form
            jTextKodeBarang.setText("");
            jTextNamaBarang.setText("");
            jTextHargaBarang.setText("");
        }
    }//GEN-LAST:event_TambahBarangActionPerformed

